#include <iostream>

int main(void) {
    
    using namespace std;

    int A = 5;
    int B = 3;

    cout << "Soma = " << A + B << endl;
    cout << "Subtração = " << A - B << endl;
    cout << "Multiplicação = " << A * B << endl;
    cout << "Divisão = " << A / B << endl;
    cout << "Resto = " << A % B  << endl;

    return 0;
}